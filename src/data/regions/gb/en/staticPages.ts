export const getStaticPageBySlug = (slug: string): StaticPage | undefined => {
  return staticPages.find(page => page.slug === slug);
};

export const getAllStaticPageSlugs = (): string[] => {
  return staticPages.map(page => page.slug);
};

export const getFooterPages = (): Pick<StaticPage, 'slug' | 'title'>[] => {
  const footerOrder = ['about', 'contact', 'privacy-policy', 'terms', 'responsible-gambling', 'affiliate-disclosure'];
  return footerOrder
    .map(slug => staticPages.find(p => p.slug === slug))
    .filter((p): p is StaticPage => p !== undefined)
    .map(({ slug, title }) => ({ slug, title }));
};
