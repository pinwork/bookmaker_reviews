export const getFAQsByCategory = (category: FAQ['category']): FAQ[] => {
  return faqs.filter(faq => faq.category === category).sort((a, b) => a.order - b.order);
};

export const getPopularFAQs = (limit: number = 10): FAQ[] => {
  return faqs.filter(faq => faq.isPopular).sort((a, b) => a.order - b.order).slice(0, limit);
};

export const getFAQBySlug = (slug: string): FAQ | undefined => {
  return faqs.find(faq => faq.slug === slug);
};

export const searchFAQs = (query: string): FAQ[] => {
  const lowerQuery = query.toLowerCase();
  return faqs.filter(faq => faq.question.toLowerCase().includes(lowerQuery) || faq.answer.toLowerCase().includes(lowerQuery));
};
